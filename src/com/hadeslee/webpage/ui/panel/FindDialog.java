    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int index = textAreaContent.lastIndexOf(content, Math.max(0, fromIndex - 1));
        if (index != -1) {
            fromIndex = index;
            jta.select(index, index + content.length());
        } else {
            JOptionPane.showMessageDialog(this, MyBundle.getString("can.not.find"));
        }
    }//GEN-LAST:event_jButton3ActionPerformed

