    private void jTextField1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextField1MousePressed
        String text = Util.getTextAreaContent(parent, jTextField1.getText());
        if (text != null) {
            content = text;
            jTextField1.setText(text);
        }
    }//GEN-LAST:event_jTextField1MousePressed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int index = textAreaContent.indexOf(content, fromIndex + content.length());
        if (index != -1) {
            fromIndex = index;
            jta.select(index, index + content.length());
        } else {
            JOptionPane.showMessageDialog(this, MyBundle.getString("can.not.find"));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

